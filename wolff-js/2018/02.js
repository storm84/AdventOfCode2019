  function contains(str, num) {
    return !!Object.entries(group(str)).find(([k, v]) => v === num);
  }

  function group(str) {
    const res = {};
    for (const x of str) {
      if (x in res) {
        res[x]++;
      } else {
        res[x] = 1;
      }
    }
    return res;
  }
